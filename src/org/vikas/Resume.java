package org.vikas;

import org.springframework.stereotype.Repository;

@Repository
public class Resume {
	private String name;
	private String date;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
